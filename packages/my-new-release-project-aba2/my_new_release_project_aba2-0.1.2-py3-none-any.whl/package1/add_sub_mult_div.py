import math

def add(a, b):
    return round(a + b, 2)

def subtraction(a, b):
    return round(a - b, 2)

def multiple(a, b):
    return round(a * b, 2)

def div(a, b):
    return round(a // b, 2)

def logariphm_two(a):
    return round(math.log(a, 2), 2)

def logariphm_natural(a):
    return round(math.log(a), 2)

def sqrt(a):
    return round(math.sqrt(a), 2)

def sqrt3(a):
    return round(a ** (1 / 3), 2)

def sqrtN(a, n):
    return round(a ** (1 / n), 2)



